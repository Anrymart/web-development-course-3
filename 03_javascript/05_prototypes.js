
// animal has methods
let animal = {
    walk() {
        if (!this.isSleeping) {
            alert(`I walk`);
        }
    },
    sleep() {
        this.isSleeping = true;
    }
};

let rabbit = {
    name: "White Rabbit",
    __proto__: animal
};


rabbit.walk();

// modifies rabbit.isSleeping
rabbit.sleep();

rabbit.walk();
